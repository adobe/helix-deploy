import assert from 'assert';
import fse from 'fs-extra';
import path from 'path';
import { createTestRoot, TestLogger } from './utils.js';

import CLI from '../src/cli.js';

describe('Azure Integration Test', () => {
  let testRoot;
  let origPwd;

  beforeEach(async () => {
    testRoot = await createTestRoot();
    origPwd = process.cwd();
  });

  afterEach(async () => {
    process.chdir(origPwd);
    // await fse.remove(testRoot);
  });

  it('Deploy to Azure (for real)', async () => {
    await fse.copy(path.resolve(__rootdir, 'test', 'fixtures', 'simple'), testRoot);

    process.chdir(testRoot); // need to change .cwd() for yargs to pickup `wsk` in package.json
    const builder = new CLI()
      .prepare([
        '--build',
        '--verbose',
        '--deploy',
        '--target', 'azure',
        '--azure-app', 'deploy-helix',
        '-p', 'FOO=bar',
        '--package.params', 'HEY=ho',
        '--update-package', 'true',
        '--test', '/foo',
        '--directory', testRoot,
        '--entryFile', 'index.js',
      ]);
    builder.cfg._logger = new TestLogger();

    const res = await builder.run();
    assert.ok(res);
    const out = builder.cfg._logger.output;
    assert.ok(out.indexOf('{"url":"https://deploy-helix.azurewebsites.net/api/simple-package/simple-name/1.45.0/foo","file":"Hello, world.\\n"}') > 0, out);
  }).timeout(150000);
});
